package com.shoperal.core.model;

public enum WeightUnit {
    /**
     * Metric system unit of mass
     */
    GRAMS,

    /**
     * 1 kilogram equals 1000 grams
     */
    KILOGRAMS,

    /**
     * Imperial system unit of mass
     */
    OUNCES,

    /**
     * 1 pound equals 16 ounces
     */
    POUNDS

}
